function getBestSellers() {
    let div = document.getElementsByClassName('best-seller-div')[0];
    
    bestSellerData.forEach((e) => {
      let div2 = document.createElement('div')
      div2.innerHTML = `<div class="my-card">
                <div class="my-card-img">
                    <img src=${e.img} alt="xxxx">
                </div>
                <div class="my-card-body">
                    <h3>${e.title}</h3>
                    <p>${e.desc}</p>
                    <p>$ ${e.price}</p>
                </div>
            </div> `
            div.appendChild(div2)
    });

}
